def _format_list(items: List[str]) -> str:
    if not items:
        return "- None recorded"
    return "\n".join(f"- {item}" for item in items)


def _bytes_human(b) -> str:
    """Convert bytes to human-readable string."""
    try:
        b = int(b)
    except (TypeError, ValueError):
        return str(b)
    if b >= 1_073_741_824:
        return f"{b:,} bytes ({b / 1_073_741_824:.2f} GB)"
    elif b >= 1_048_576:
        return f"{b:,} bytes ({b / 1_048_576:.0f} MB)"
    elif b >= 1024:
        return f"{b:,} bytes ({b / 1024:.0f} KB)"
    return f"{b:,} bytes"


def write_outputs(state: AnalysisState) -> None:
    output_dir = Path(state.work_dir)

    # findings.json
    findings_path = output_dir / "findings.json"
    findings_path.write_text(
        json.dumps({key: value.to_dict() for key, value in state.findings.items()}, indent=2),
        encoding="utf-8",
    )

    # agent_log.json
    agent_log_path = output_dir / "agent_log.json"
    agent_log_path.write_text(json.dumps(state.agent_log, indent=2), encoding="utf-8")

    # report.md — comprehensive forensic report (always written, overwrites any
    # prior LLM-synthesized report since the template version includes timeline,
    # IOC table, MITRE mapping, and recommendations that the LLM version may lack)
    report_md = _build_detailed_report(state, output_dir)
    report_path = output_dir / "report.md"
    report_path.write_text(report_md, encoding="utf-8")


