def _extract_iocs(findings: Dict[str, Finding]) -> List[dict]:
    """Extract IOCs from all findings' evidence items."""
    import ipaddress
    iocs: List[dict] = []
    seen = set()

    _rfc1918 = [
        ipaddress.ip_network("10.0.0.0/8", strict=False),
        ipaddress.ip_network("172.16.0.0/12", strict=False),
        ipaddress.ip_network("192.168.0.0/16", strict=False),
    ]

    def _is_external(ip: str) -> bool:
        try:
            addr = ipaddress.ip_address(ip)
            return not any(addr in net for net in _rfc1918)
        except ValueError:
            return False

    def _is_internal(ip: str) -> bool:
        try:
            addr = ipaddress.ip_address(ip)
            return any(addr in net for net in _rfc1918)
        except ValueError:
            return False

    for key in ["A", "B", "C", "D"]:
        finding = findings.get(key)
        if not finding:
            continue
        directive = INVESTIGATION_DIRECTIVES.get(key, {})
        phase_name = directive.get("title", key)

        for item in finding.evidence:
            # External IPs
            for ip in [item.src_ip, item.dst_ip]:
                if ip and _is_external(ip) and ip not in seen:
                    seen.add(ip)
                    context = item.description[:80] if item.description else "—"
                    iocs.append({
                        "type": "IP Address",
                        "value": ip,
                        "context": context,
                        "phase": phase_name,
                        "first_seen": item.ts or "—",
                    })

            # Key internal hosts (patient zero, DCs, file servers, targets)
            for ip in [item.src_ip, item.dst_ip]:
                if ip and _is_internal(ip) and ip not in seen:
                    # Include internal IPs in significant contexts
                    desc_lower = (item.description or "").lower()
                    if any(kw in desc_lower for kw in [
                        "patient zero", "pivot", "domain controller", "dc",
                        "file server", "exfil", "beachhead", "deployment",
                        "kerberos", "backup", "veeam", "credential",
                        "dcsync", "drsgetncchanges", "backupkey",
                        "ransomware", "executable", "hfs.exe", "kkwlo",
                    ]):
                        seen.add(ip)
                        context = item.description[:80] if item.description else "—"
                        iocs.append({
                            "type": "Internal Host",
                            "value": ip,
                            "context": context,
                            "phase": phase_name,
                            "first_seen": item.ts or "—",
                        })

            # Domains from description
            desc = item.description or ""
            for domain_keyword in ["temp.sh", "file.io", "transfer.sh", "mega.nz"]:
                if domain_keyword in desc.lower() and domain_keyword not in seen:
                    seen.add(domain_keyword)
                    iocs.append({
                        "type": "Domain",
                        "value": domain_keyword,
                        "context": desc[:80],
                        "phase": phase_name,
                        "first_seen": item.ts or "—",
                    })

            # Filenames from description
            for filename_keyword in [
                "hfs.exe", "kkwlo.exe", "Microsofts.exe", "delete.me",
                "HOW TO BACK FILES", "UninstallWinClient.exe",
                "hfs.ips.txt", "SETUP.EXE", "arrestees.zip", "incidents.zip",
                "user_db_export", "Groups.xml", "Registry.xml",
            ]:
                if filename_keyword in desc and filename_keyword not in seen:
                    seen.add(filename_keyword)
                    iocs.append({
                        "type": "Filename",
                        "value": filename_keyword,
                        "context": desc[:80],
                        "phase": phase_name,
                        "first_seen": item.ts or "—",
                    })

            # RDP cookies from description
            for cookie_keyword in ["lgallegos", "Administr", "WATER\\Adm", "WATER\\LGallegos", "jjjjjjjRD", "ecoborrow", "Springfi3"]:
                if cookie_keyword in desc and cookie_keyword not in seen:
                    seen.add(cookie_keyword)
                    iocs.append({
                        "type": "RDP Cookie / Username",
                        "value": cookie_keyword,
                        "context": desc[:80],
                        "phase": phase_name,
                        "first_seen": item.ts or "—",
                    })

    return iocs
